module OneD_Order4


function evaluate(x::Float64)
    phi=zeros(5,1)
    dphi=zeros(5,1)
phi[1]=2/3*(x+1/2)*x*(x-1/2)*(x-1)
phi[2]=2/3*(x+1)*(x+1/2)*x*(x-1/2)




phi[3]=-8/3*(x+1)*x*(x-1/2)*(x-1)
phi[4]=4*(x+1)*(x+1/2)*(x-1/2)*(x-1)
phi[5]=-8/3*(x+1)*(x+1/2)*x*(x-1)

dphi[1]=x*(2*x/3 + 1/3)*(x - 1) + x*(2*x/3 + 1/3)*(x - 1/2) + 2*x*(x - 1)*(x - 1/2)/3 + (2*x/3 + 1/3)*(x - 1)*(x - 1/2)
dphi[2]=x*(2*x/3 + 2/3)*(x - 1/2) + x*(2*x/3 + 2/3)*(x + 1/2) + 2*x*(x - 1/2)*(x + 1/2)/3 + (2*x/3 + 2/3)*(x - 1/2)*(x + 1/2)
dphi[3]=x*(-8*x/3 - 8/3)*(x - 1) + x*(-8*x/3 - 8/3)*(x - 1/2) - 8*x*(x - 1)*(x - 1/2)/3 + (-8*x/3 - 8/3)*(x - 1)*(x - 1/2)
dphi[4]=4*(x - 1)*(x - 1/2)*(x + 1/2) + (x - 1)*(x - 1/2)*(4*x + 4) + (x - 1)*(x + 1/2)*(4*x + 4) + (x - 1/2)*(x + 1/2)*(4*x + 4)

dphi[5]=x*(-8*x/3 - 8/3)*(x - 1) + x*(-8*x/3 - 8/3)*(x + 1/2) - 8*x*(x - 1)*(x + 1/2)/3 + (-8*x/3 - 8/3)*(x - 1)*(x + 1/2)

return phi,dphi

end











end
